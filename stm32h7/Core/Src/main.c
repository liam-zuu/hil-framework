/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_SPI2_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_TIM15_Init();
  MX_TIM16_Init();
  MX_TIM17_Init();

  /* USER CODE BEGIN 2 */

  g_pclk2  = HAL_RCC_GetPCLK2Freq();
  g_ftimer = 2 * g_pclk2 / 240;  /* 2×PCLK2 / (PSC+1) */

  EncoderGen_Init(&htim1, &htim2, &htim3, &htim4);
  SPI1_Handler_Init(&hspi1);
  PWMCapture_Init(&htim5, &htim15, &htim16, &htim17);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

	    if (SPI1_IsFrameReady()) {
	            SPI1_GetState(&g_state);

	            /* Cập nhật encoder gen cho 4 bánh */
	            for (int i = 0; i < 4; i++) {
	                EncoderGen_SetOmega(i, g_state.omega[i]);
	            }

	            /* Đọc PWM từ ESP32, điền vào g_output */
	            PWMCapture_GetDuty(g_output.pwm);

	            /* DEBUG: gửi enc_oc_count về RPi5 qua byte 16-17 MISO */
	            g_output.status = (uint8_t)((enc_oc_count >> 8) & 0xFF);
	            g_output.seq    = (uint8_t)(enc_oc_count & 0xFF);

	            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin,
	                              (g_state.omega[0] > 0.5f) ? GPIO_PIN_SET
	                                                         : GPIO_PIN_RESET);
	            SPI1_SetOutput(&g_output);
	        }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

